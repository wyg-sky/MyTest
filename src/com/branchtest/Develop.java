package com.branchtest;

public class Develop {
    private String projectName;
    private Integer projectCycle;
    private Integer projectPeople;
    private Double projectFunds;
    private String projectDescription;
    public String getProjectName() {
        return projectName;
    }
    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }
    public Integer getProjectCycle() {
        return projectCycle;
    }
    public void setProjectCycle(Integer projectCycle) {
        this.projectCycle = projectCycle;
    }
    public Integer getProjectPeople() {
        return projectPeople;
    }
    public void setProjectPeople(Integer projectPeople) {
        this.projectPeople = projectPeople;
    }
    public Double getProjectFunds() {
        return projectFunds;
    }
    public void setProjectFunds(Double projectFunds) {
        this.projectFunds = projectFunds;
    }
    public String getProjectDescription() {
        return projectDescription;
    }
    public void setProjectDescription(String projectDescription) {
        this.projectDescription = projectDescription;
    }

}
